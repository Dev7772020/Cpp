#include<iostream>
using namespace std;

int main(){
    /*
    There are three types of loops:
    1) For Loop
    2) While Loop
    3) do-While Loop
    */
    for (int n = 1; n < 10 ;n++)
    {
        cout<<n<<endl;
    }

    cout<<endl;

    int a=0;
    while(a<=20){
        cout<<a<<endl;
        a++;
    }
    return 0;
}